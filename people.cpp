#include "People.h"

/*******************************************************************************************************
**										People Default Constructor
**						Constructs the object with empty variables
*******************************************************************************************************/
People::People()
{
	personName = "";
	personAge = 0;
	personScore = 0.0;
}


/*******************************************************************************************************
**										People Constructor
**					Constructs the object with user input for each variable
*******************************************************************************************************/
People::People(string n, int a, double g)
{
	personName = n;
	personAge = a;
	personScore = g;
}


/*******************************************************************************************************
**										People destructor
**									Destructs the object
*******************************************************************************************************/
People::~People()
{

}


/*******************************************************************************************************
**										getName function
**									returns the name of person variable
*******************************************************************************************************/
string People::getName()
{
	return this->personName;
}


/*******************************************************************************************************
**										getAge function
**									returns the age of person variable
*******************************************************************************************************/
int People::getAge()
{
	return this->personAge;
}


/*******************************************************************************************************
**										getScore function
**								returns the score of person variable
*******************************************************************************************************/
double People::getScore()
{
	return this->personScore;
}


/*******************************************************************************************************
**										do_work function
**				creates a random number between 1 and the user input and stores it
*******************************************************************************************************/
void People::do_work(int x)
{
	hoursWorked = rand() % x + 1;
}